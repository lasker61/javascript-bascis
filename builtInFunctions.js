//abs() function shows any numbers to its absolute value!
var number = -8376;
var absoluteNumber = Math.abs(number);
console.log(absoluteNumber);

// round() function shows only round figures
var price = 4.4665;
var roundPrice = Math.round(price);
console.log(roundPrice);

//ceil() function shows the upper round figures only
var score = 7.35;
var ceilScore = Math.ceil(score);
console.log(ceilScore);

// floor() function shows thw lower round figure only
var cgpa = 3.8765;
var cgpaFloor = Math.floor(cgpa);
console.log(cgpaFloor);

//random number

var randomNumber = 8;
var calculateRandomNumber = Math.random(randomNumber);
console.log(calculateRandomNumber);